import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const cwd = process.cwd();
const articlesPath = resolve(cwd, "src/data/articles.ts");
const outPath = resolve(cwd, "src/data/articleSlugs.ts");

const src = readFileSync(articlesPath, "utf8");

const listStart = src.indexOf("export const articleList: ArticleMeta[] = [");
if (listStart === -1) {
  console.error("Could not find `articleList` export in articles.ts");
  process.exit(1);
}

const listSection = src.slice(listStart);

const slugs = [];
const re = /\bslug:\s*"([a-z0-9][a-z0-9-]*)"/g;
let m;
while ((m = re.exec(listSection)) !== null) {
  slugs.push(m[1]);
}

if (slugs.length === 0) {
  console.error("No slugs extracted — regex may be out of sync with articles.ts format");
  process.exit(1);
}

const unique = Array.from(new Set(slugs));

const content = `// AUTO-GENERATED by \`npm run generate:article-slugs\`.
// Source of truth: src/data/articles.ts (articleList).
// Do not edit by hand — regenerate when articles change.

export const ARTICLE_SLUGS: readonly string[] = ${JSON.stringify(unique, null, 2)} as const;
`;

writeFileSync(outPath, content);
console.log(`Generated ${unique.length} article slugs → ${outPath}`);
